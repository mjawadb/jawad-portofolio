-- 6. QUERY UNTUK MERESET DAN MERAPIKAN ID (Jalankan sekali saja jika benar-benar perlu)
-- Ganti 'messages' dengan nama tabel kamu yang sebenarnya (misal: 'blogs' atau 'messages')
