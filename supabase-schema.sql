-- 5. Auto-Cleanup Script for Messages (Spam/Storage Protection)
-- Create a function to clean up old messages and keep size in check
CREATE OR REPLACE FUNCTION cleanup_old_messages()
RETURNS void AS $$
BEGIN
  -- 1. Hapus pesan yang usianya lebih dari 1 bulan
  DELETE FROM public.messages WHERE created_at < now() - interval '1 month';
  
  -- 2. Jika ukuran tabel messages masih lebih dari 300MB, 
  --    hapus sisanya dan hanya sisakan 5000 pesan terbaru untuk menghemat tempat.
  IF pg_total_relation_size('public.messages') > 300 * 1024 * 1024 THEN
    DELETE FROM public.messages
    WHERE id NOT IN (
      SELECT id FROM public.messages ORDER BY created_at DESC LIMIT 5000
    );
  END IF;
END;
$$ LANGUAGE plpgsql;

-- Aktifkan ekstensi pg_cron (jika belum aktif)
CREATE EXTENSION IF NOT EXISTS pg_cron;

-- Jadwalkan fungsi cleanup untuk berjalan otomatis setiap hari pada jam 00:00 (tengah malam)
-- Dengan berjalan tiap hari, database akan dicek terus agar tidak kepenuhan.
SELECT cron.schedule('cleanup-messages-daily', '0 0 * * *', $$ SELECT cleanup_old_messages(); $$);

